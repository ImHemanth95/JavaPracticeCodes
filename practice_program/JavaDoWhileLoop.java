package practice_program;

public class JavaDoWhileLoop {

	public static void main(String[] args) {
		//do while- executes the programm several time and if the 
		//iterations is not fixed then you need to execute the loop
		//at least once
		int i = 1;
		do {
			System.out.print(i);
			i++;
		}
		while(i<=10);
		
	}

}
